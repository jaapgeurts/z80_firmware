printd: ; push it into the buffer; then redraw the screen
  push hl
  push bc
  push de

  ld   de,(v_cursor) ; remember start pos
  ld   (v_tmp),de

  ; add cursor index  to screenbuf start
  push hl
  ld   hl,v_screenbuf
  add  hl,de
  ex   de,hl
  pop  hl

;   ; calculate end position
;   ld   b,0
;   ld   c,(hl)
;   push hl
;   ld   hl,(v_cursor)
;   add  hl,bc
;   ld   (v_tmp2),hl
;   pop  hl

  ld   b,(hl)
.printd_loop:
  inc  hl
  ld   a, (hl)

  ; check for CR and LF
  cp   CR
  jr   nz, .checkLF
  ; move cursor to home
  ; get remainder
  push hl ; store the str pointer
  push bc ; store the str index counter
  push de ; ld hl,de
  pop  hl ; hl contains the v_cursor
  ; TODO: mask off alignment instead of division
  ld   c,COLS
  call division ; a = remainder
  scf
  ccf ; clear carry flag
  ld   b,0
  ld   c,a
  ex   de,hl ; ld hl with v_cursor(de)
  sbc  hl,bc
  ex   de,hl ; ld result back into v_cursor(de)
  pop  bc  ; restore the str index counter
  pop  hl  ; restore the str pointer
  jr   .endif
.checkLF:
  cp   LF
  jr   nz,.storeChar
  push hl
  ld   hl,COLS
  add  hl,de ; TODO: scroll screen if de > 1200
  ex   de,hl
  pop  hl
  jr   .endif
.storeChar:
  ld   (de),a
  inc  de
.endif:
  djnz .printd_loop

  ; subtract screenbuf
  scf
  ccf
  ld   hl,v_screenbuf
  ex   de,hl ; 
  sbc  hl,de
  ld   (v_cursor),hl

  ld   bc,(v_tmp)
  ld   de,(v_cursor)

  call displayRepaint

  pop  de
  pop  bc
  pop  hl

  ret

; bc: start, de: end
displayRepaint:
  push hl
  push bc
  push de

  ; draw from start(bc) to end(de)
  ld   hl,v_screenbuf
  add  hl,de ; save end location
  ld   (v_tmp),hl

  ; calculate startx = (start%60 ) * fontw
  ; calculate starty = (start/60) * fonth
  push bc
  push bc
  pop  hl ; ld hl,bc
  ld   c,60
  call division ; hl / c = hl rem a
  push hl ; push quotient (y)
  ; calc x
  ld   b,a
  ld   c,FONTW
  call multiply ; result in hl
  ld   (vt_xstart),hl
  ; calc y
  pop  hl
  ld   b,l
  ld   c,FONTH
  call multiply ; result in hl
  ld   (vt_ystart),hl
  ; go through the array. if dirty draw
  pop  bc
  ld   hl,v_screenbuf
  add  hl,bc
  push hl
  pop  bc ; ld bc,hl: bc contains start location in screenbuf

.nextGlyph:

  ; set display start and end position
  push bc
  ld   hl,(vt_xstart)
  push hl ; save start
  ld   bc,FONTW-1
  add  hl,bc
  ex   de,hl ; end in de
  pop  hl ; restore start
  call displaySetX1X2  ; from hl -> de
  ld   hl,(vt_ystart)
  push hl   ; save start
  ld   bc,FONTH-1
  add  hl,bc
  ex   de,hl ; end in de
  pop  hl
  call displaySetY1Y2
  ld   a,ILI_MEM_WRITE    ; do write
  out  (TFT_C),a
  pop  bc

  ld   a,(bc) ; load letter
  
  ; get the glyph
  ld   de,BYTESPERFONT  ; font small
  ld   hl,allletters
.findGlyph
  cp   0
  jr   z,.drawGlyph
  add  hl,de
  dec  a
  jr   nz,.findGlyph
  ; hl now points to the correct glyph

.drawGlyph:

  push bc ; contains the index into the screenbuf

  ; pixels to set
  ld   b,BYTESPERFONT ; font small
.next_byte:
  ld   a,(hl)
  ld   c, 8
.shift_bit:
  ld   d,a
  ;and  0x80
  bit  7,a
  jr   z,.pix_off
  ld   a,0xff   ; foreground white
  out  (TFT_D),a
;  ld   a,0xff
  out  (TFT_D),a
  jr   .continue
.pix_off 
  ld   a,0xf8  ; background blue TODO: move to var
  out  (TFT_D),a
  ld   a,0x00
  out  (TFT_D),a
.continue: ;
  ld   a,d
  sla  a
  dec  c
  jr   nz,.shift_bit
  inc  hl
  djnz .next_byte

  pop  bc
  
  ; done with the glyph. goto next cell
  inc  bc   ; if bc < 1200 continue at .incxy
  ld   de,(v_tmp)
  ld   a,b
  cp   d
  jr   nz,.incxy
  ld   a,c
  cp   e
  jr   nz,.incxy
  ; done drawing
  jr   .printLetEnd

.incxy:

  ld  hl,(vt_xstart)
  ld  de,FONTW
  add hl,de
  ld  a,h
  cp  1
  jr  nz, .next1
  ld  a,l
  cp  0xe0  ; one beyond the last column
  jr  nz, .next1
  ; over edge; increase y and set x to zero
  ; increase y
  ld  hl,(vt_ystart)
  ld  de,FONTH
  add hl,de
  ld  (vt_ystart),hl
  ld  hl,0  ;set x-start to zero
.next1:
  ld  (vt_xstart),hl
  jp  .nextGlyph

.printLetEnd:
  pop  de
  pop  bc
  pop  hl
  ret

