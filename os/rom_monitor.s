; parses an address string into hl
; input: HL : string
; returns address in HL
getAddress:
  push de
  pop  hl ; ld hl,de
  call nextToken
  ld   a,c
  cp   0 ; no argument given.
  jr   nz,.getadr_start
  ld   hl,argerror_msg
  call printk
  cp   a ; set zero flag
  ret

.getadr_start:
  push bc
  push de
  push hl
  pop  de
  inc  de
  ; parse it
  ld   a,(de)
  ld   b,a
  inc  de
  ld   a,(de)
  ld   c,a
  call parseHexStr
  ld   h,a
  inc  de
  ld   a,(de)
  ld   b,a
  inc  de
  ld   a,(de)
  ld   c,a
  call parseHexStr
  ld   l,a
  pop  de
  pop  bc
  or   1; reset zero flag
  ret


