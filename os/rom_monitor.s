;   hl = b * c
multiply:
  push de
  push bc
  ld   hl,0
  ld   a,b
  or   a
  jr   z,.end
  ld   d,0
  ld   e,c
.mul_loop:
  add  hl,de
  djnz .mul_loop
.end:
  pop  bc
  pop  de
  ret

; hl by c, quotient in hl, remainder in a
division:
  push bc
  xor	a
  ld	b, 16

.loop:
  add	hl, hl
  rla
  jr	c, $+5
  cp	c
  jr	c, $+4

  sub	c
  inc	l
   
  djnz	.loop
  pop   bc 
  ret


getKeyWait:
  call getKey
  jr   z, getKeyWait
  ret

; result in a, if no data available zero bit is set
getKey:
  push hl
  push de
  di                        ; disable interrupts
  ; compare if buffer is empty
  ld   hl, (keyb_buf_wr)
  ld   de, (keyb_buf_rd) ; read pointer
  ld   a,l 
  cp   e                     ; is it equal then buffer is empty
  jr   z,.getKey_end
.getKey_take:
  ld   a,(de) ; read from position
  push af
  inc  e
  ld   a,e
  and  SER_BUF_SIZE-1
  ld   e,a
  ld  (keyb_buf_rd),de ; pointer pointer back into mem
  pop  af
.getKey_end:
  ei
  pop  de
  pop  hl
  ret

putKey:
  push hl
  push de
  push bc
  ld   b,a
  ; begin
  ld   hl,(keyb_buf_wr)
  ld   de,(keyb_buf_rd)
  dec  e
  ld   a,e
  and  SER_BUF_SIZE-1
  ld   e,a
  ld   a,l
  cp   e
  jr   z, .putKey_end  ; head = tail -1 => buffer full
.putKey_put
  ld   hl,(keyb_buf_wr)
  ld   (hl),b
  inc  l
  ld   a,l
  and  SER_BUF_SIZE-1
  ld   l,a
  ld   (keyb_buf_wr),hl
.putKey_end:
  pop  bc
  pop  de
  pop  hl
  ret

putChar:
  push af
  call putSerialChar
  pop  af
  call putDisplayChar
  ret

; print char to lcd
putDisplayChar: 
  push hl
  push de
  push bc

  ld   hl,v_screenbuf
  ld   de,(v_cursor)
  add  hl,de
 
  cp   BS
  jr   nz,.checkCR
  ; handle BS - remove previous char
  ; TODO: make sure de != 0
  dec  de
  ld   (v_cursor),de
  jr   .end_nodraw

.checkCR:
  cp   CR
  jr   nz,.checkLF
  ; recalculate the cursor position
  ld   c,COLS
  push de
  ex   de,hl ; put cursor in hl
  call division ; a = remainder
  scf
  ccf ; clear carry flag
  ld   b,0
  ld   c,a
  pop  hl
  sbc  hl,bc
  ld   (v_cursor),hl
  jr   .end_nodraw

.checkLF:
  cp   LF
  jr   nz,.placeChar
  ;   line feed
  ld   hl,COLS
  add  hl,de ; TODO: scroll screen if de > 1200
  ld   (v_cursor),hl
  jr   .end_nodraw

.placeChar
  push de  ; store start location
  ld   (hl),a
  inc  de
  ld   (v_cursor),de

  pop  bc ; load original cursor back
  ; update screen
  ; TODO: scroll screen when necessary
  call displayRepaint ; bc start; de end
.end_nodraw:
  pop  bc
  pop  de
  pop  hl
  ret

putSerialChar:
  push af
  call waitSerialTX  ; make sure we can send
  pop  af
  out  (SIO_AD), a
  ret

getSerialChar:
; check if character available
  ld   a, 0b00000000 ; write to WR0. Next byte is RR0
  out  (SIO_AC), a
  in   a, (SIO_AC)
  bit  0, a
  ret  z  ; no char available
; if yes, then read and return in a
  in   a,(SIO_AD)
  ret

getSerialCharWait:
; check if character available
  ld   a, 0b00000000 ; write to WR0. Next byte is RR0
  out  (SIO_AC), a
  in   a, (SIO_AC)
  bit  0, a
  jr   z,getSerialCharWait  ; no char available
; if yes, then read and return in a
  in   a,(SIO_AD)
  ret

waitSerialTX:  ; wait for serial port to be free
  ld   a, 0b00000000 ; write to WR0. Next byte is RR0
  out  (SIO_AC), a
  in   a, (SIO_AC)
  bit  2,a
  jr   z, waitSerialTX
  ret

  ; init the serial port
initSerialConsole:
; reset channel 0
  ld	a, 0b00110000
  out (SIO_AC), a

; prepare for writing WR4 - datasheet says write to WR4 first then other registers
  ld	a, 0b00000100 ; write to WR0. Next byte is WR4
  out	(SIO_AC), a
  ld	a, 0b01000100               ; 16x prescaler, No parity, 1 stopbit
  out	(SIO_AC), a

; enable interrupt on char (WR1)
  ld	a, 0b00000001 ; 
  out	(SIO_AC), a
  ld	a, 0b00011000 ; int on all Rx chars
  out	(SIO_AC), a

; enable receive (WR3)
  ld	a, 0b00000011
  out	(SIO_AC), a
  ld	a, 0b11000001             ; recv enable; 8bits / char
  out	(SIO_AC), a

; write register 5
  ld	a, 0b00000101
  out	(SIO_AC), a
  ld	a, 0b01101000            ; send enable
  out	(SIO_AC), a

  ret

initCtc:
  push af ; store af. it contains the baud rate time constant
; clock is 3,686,400 Hz
; clock frequency of the CTC must be 2x trigger frequency in other words:
; input frequency on TRG0 is 1,843,200Hz (must be at least than half clock freq)
  ld a, 0b01010101 ; control register, external trigger, counter mode, rising edge 
  out  (CTC_A), a
  ; baudrates - Time constant @ 1.8432 MHz
  ; 9600      - 12
  ; 19200     - 6
  ; 57600     - 2
  ; 115200    - 1
  ;ld a, 1 ; 115200 @ 1.8432 MHz
  pop  af
  out  (CTC_A), a
  ret

; KEYBOARD FUNCTIONS

handleKeyboard:

  push hl
  push bc
  ; translate scan code
  ; ignore release codes
  cp   0xf0 ; break code
  jr   nz, .check7bit
.read_kbd_break:
  call getKeyboardChar  ;read the next char
  cp   L_SHIFT
  jr   z,.read_kbd_unshifted:
  cp   R_SHIFT
  jr   nz,.read_kbd_end:
.read_kbd_unshifted:
  ld   a,0
  ld   (v_shifted),a
  jr   .read_kbd_end

.check7bit:
  ; if larger than 80; just store it otherwise translate
  cp   0x80
  jr   nc, .store
  push af
  cp   L_SHIFT
  jr   z,.read_kbd_set_shifted:
  cp   R_SHIFT
  jr   nz,.read_kbd_fetch:
.read_kbd_set_shifted:
  ld   a,1
  ld   (v_shifted),a
  pop  af
  jr   .read_kbd_end
 
.read_kbd_fetch:
  ld   hl,trans_table_normal
  ld   a,(v_shifted)
  cp   1
  jr   nz,.read_kbd_fetch_2
  ld   hl,trans_table_shifted
.read_kbd_fetch_2:
  pop  af
  ld   b, 0
  ld   c, a
  add  hl, bc
  ld   a,(hl)

.store:
  call putKey  ; store the key in the ring buffer

.read_kbd_end:
  pop  bc
  pop  hl
  ret

